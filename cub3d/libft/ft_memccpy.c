#include "libft.h"

void	*ft_memccpy(void *d, const void *s, int c, size_t n)
{
	const char	*src;
	char		*dest;

	dest = (char *)d;
	src = (char *)s;
	while (n--)
	{
		*(dest++) = *src;
		if (*(src++) == (char)c)
			return (dest);
	}
	return (0);
}
