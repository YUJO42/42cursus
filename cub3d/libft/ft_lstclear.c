#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp1;
	t_list	*tmp2;

	tmp2 = *lst;
	while (tmp2)
	{
		tmp1 = tmp2;
		tmp2 = tmp1->next;
		del(tmp1->content);
		free(tmp1);
	}
	*lst = NULL;
}
