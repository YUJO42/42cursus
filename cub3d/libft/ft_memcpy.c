#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t num)
{
	unsigned int	idx;

	idx = 0;
	while (idx < num && !(!src && !dst && num))
	{
		*(char*)(dst + idx) = *(char*)(src + idx);
		idx++;
	}
	return (dst);
}
