#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*tmp;

	tmp = lst;
	if (tmp == 0)
		return (0);
	while (tmp[0].next != NULL)
		tmp = tmp[0].next;
	return (tmp);
}
