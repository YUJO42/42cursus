#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	index;
	char			*tmp;

	tmp = (char*)s;
	index = 0;
	while (index < n)
	{
		*(tmp + index) = 0;
		index++;
	}
}
